package edu.pace.cs;

public class Character {
	
	private Integer id;	
	private String alter_ego;
	private String real_name;
	private String know_more;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getAlter_ego() {
		return alter_ego;
	}
	public void setAlter_ego(String alter_ego) {
		this.alter_ego = alter_ego;
	}
	public String getReal_name() {
		return real_name;
	}
	public void setReal_name(String real_name) {
		this.real_name = real_name;
	}
	public String getKnow_more() {
		return know_more;
	}
	public void setKnow_more(String know_more) {
		this.know_more = know_more;
	}

}
